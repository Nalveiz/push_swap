#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*a;
	size_t			i;

	a = (unsigned char *)s;
	i = 0;
	while (i < n)
	{
		if ((unsigned char)c == a[i])
			return ((&a[i]));
		i++;
	}
	return (0);
}
